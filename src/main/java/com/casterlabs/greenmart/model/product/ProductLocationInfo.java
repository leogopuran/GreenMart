package com.casterlabs.greenmart.model.product;

public class ProductLocationInfo {

    private String productLocationAddress;
    private String productLocationLat;
    private String productLocationLng;

    public String getProductLocationAddress() {
        return productLocationAddress;
    }

    public void setProductLocationAddress(String productLocationAddress) {
        this.productLocationAddress = productLocationAddress;
    }

    public String getProductLocationLat() {
        return productLocationLat;
    }

    public void setProductLocationLat(String productLocationLat) {
        this.productLocationLat = productLocationLat;
    }

    public String getProductLocationLng() {
        return productLocationLng;
    }

    public void setProductLocationLng(String productLocationLng) {
        this.productLocationLng = productLocationLng;
    }
}
